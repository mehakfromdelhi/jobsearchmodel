"use client";

import { useEffect, useMemo, useState } from "react";

export function AnalysisRunView({ run }) {
  const [draftState, setDraftState] = useState("idle");
  const [draftMessage, setDraftMessage] = useState("");
  const [draftsByPairId, setDraftsByPairId] = useState({});

  const roleMap = useMemo(
    () => Object.fromEntries((run?.results?.roles || []).map((role) => [role.id, role])),
    [run]
  );
  const resumeMap = useMemo(
    () => Object.fromEntries((run?.results?.resumeInputs || []).map((resume) => [resume.id, resume])),
    [run]
  );
  const bestPairs = useMemo(() => getBestPairsForRun(run), [run]);

  useEffect(() => {
    let cancelled = false;

    async function autoGenerateDrafts() {
      setDraftsByPairId({});
      if (!run?.id || !bestPairs.length) return;

      setDraftState("loading");
      setDraftMessage("");

      const nextDrafts = {};
      for (const pair of bestPairs) {
        const role = roleMap[pair.roleId];
        const resume = resumeMap[pair.resumeId];
        if (!role || !resume) continue;

        try {
          const response = await fetch(`/api/analysis/${run.id}/resume-draft`, {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({
              role,
              resume,
              strengths: pair.strengths,
              gaps: pair.gaps,
              missingKeywords: pair.missingKeywords
            })
          });
          const json = await response.json();

          if (!response.ok) {
            nextDrafts[pair.pairId] = {
              title: `${resume.name} - ${role.company}`,
              content: "",
              error: json.error || "Could not generate a revised draft."
            };
            continue;
          }

          nextDrafts[pair.pairId] = {
            title: json.title || `${resume.name} - ${role.company}`,
            content: json.content || "",
            error: ""
          };
        } catch (error) {
          nextDrafts[pair.pairId] = {
            title: `${resume.name} - ${role.company}`,
            content: "",
            error: error?.message || "Could not generate a revised draft."
          };
        }
      }

      if (cancelled) return;

      setDraftsByPairId(nextDrafts);
      setDraftState("ready");
      setDraftMessage("The app generated one revised draft per role using the best-matching resume.");
    }

    autoGenerateDrafts();
    return () => {
      cancelled = true;
    };
  }, [bestPairs, resumeMap, roleMap, run]);

  async function refreshDraft(pair) {
    const role = roleMap[pair.roleId];
    const resume = resumeMap[pair.resumeId];
    if (!role || !resume) return;

    setDraftState("loading");
    setDraftMessage("");

    const response = await fetch(`/api/analysis/${run.id}/resume-draft`, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        role,
        resume,
        strengths: pair.strengths,
        gaps: pair.gaps,
        missingKeywords: pair.missingKeywords
      })
    });

    const json = await response.json();
    if (!response.ok) {
      setDraftState("error");
      setDraftMessage(json.error || "Could not create the revised resume.");
      return;
    }

    setDraftsByPairId((current) => ({
      ...current,
      [pair.pairId]: {
        title: json.title || `${resume.name} - ${role.company}`,
        content: json.content || "",
        error: ""
      }
    }));
    setDraftState("ready");
    setDraftMessage("Revised draft refreshed.");
  }

  async function downloadDocx(draft) {
    const response = await fetch("/api/export/docx", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        title: draft.title,
        content: draft.content
      })
    });

    if (!response.ok) {
      const json = await response.json();
      setDraftMessage(json.error || "Could not export the Word document.");
      setDraftState("error");
      return;
    }

    const blob = await response.blob();
    const objectUrl = URL.createObjectURL(blob);
    const link = document.createElement("a");
    link.href = objectUrl;
    link.download = `${slugify(draft.title || "revised-resume")}.docx`;
    link.click();
    URL.revokeObjectURL(objectUrl);
  }

  if (!run) return null;

  return (
    <div className="stack">
      <section className="card">
        <div className="section-head">
          <div>
            <p className="eyebrow">Analysis overview</p>
            <h3>{run.topRecommendation}</h3>
            <p>
              {run.resumeCount} resume{run.resumeCount === 1 ? "" : "s"} x {run.roleCount} role
              {run.roleCount === 1 ? "" : "s"} analyzed on {run.createdAt}
            </p>
          </div>
        </div>
        <div className="card-grid">
          <div className="card stat-card">
            <p className="eyebrow">Analysis mode</p>
            <strong>{labelForMode(run.analysisMode)}</strong>
            <p>Selected before this run started</p>
          </div>
          <div className="card stat-card">
            <p className="eyebrow">ATS matches</p>
            <strong>{run.atsMatchCount}</strong>
            <p>Pairs above the ATS threshold</p>
          </div>
          <div className="card stat-card">
            <p className="eyebrow">HR matches</p>
            <strong>{run.hrMatchCount}</strong>
            <p>Pairs above the recruiter-fit threshold</p>
          </div>
          <div className="card stat-card">
            <p className="eyebrow">Submitted URLs</p>
            <strong>{run.submittedUrls.length}</strong>
            <p>Role links included in this run</p>
          </div>
        </div>
      </section>

      <section className="card">
        <p className="eyebrow">By role</p>
        <h3>Best matching resume for each job</h3>
        <div className="table-wrap">
          <table className="data-table">
            <thead>
              <tr>
                <th>Company</th>
                <th>Role</th>
                <th>ATS matches</th>
                <th>HR matches</th>
                <th>Best resume</th>
                <th>Source</th>
              </tr>
            </thead>
            <tbody>
              {(run.results.roles || []).map((role) => (
                <tr key={role.id}>
                  <td>{role.company}</td>
                  <td>{role.role}</td>
                  <td>{role.atsMatchCount}</td>
                  <td>{role.hrMatchCount}</td>
                  <td>{role.bestResumeName || "No strong match yet"}</td>
                  <td>
                    <a href={role.sourceUrl} className="text-link" target="_blank" rel="noreferrer">
                      Open URL
                    </a>
                  </td>
                </tr>
              ))}
            </tbody>
          </table>
        </div>
      </section>

      <section className="card">
        <p className="eyebrow">Fit matrix</p>
        <h3>Resume-by-role scorecard</h3>
        <div className="table-wrap">
          <table className="data-table">
            <thead>
              <tr>
                <th>Resume</th>
                <th>Role</th>
                <th>ATS</th>
                <th>HR</th>
                <th>Recommendation</th>
                <th>Highlights</th>
                <th>Draft</th>
              </tr>
            </thead>
            <tbody>
              {(run.results.pairs || []).map((pair) => {
                const isBestForRole = bestPairs.some((item) => item.pairId === pair.pairId);
                return (
                  <tr key={pair.pairId}>
                    <td>{pair.resumeName}</td>
                    <td>
                      <strong>{pair.roleName}</strong>
                      <div className="muted-text">{pair.company}</div>
                    </td>
                    <td>{pair.atsScore}</td>
                    <td>{pair.hrScore}</td>
                    <td>{pair.recommendation}</td>
                    <td>
                      <div className="stack-tight">
                        <span>{pair.strengths[0] || "No major strength detected yet."}</span>
                        {pair.gaps[0] ? <span>{pair.gaps[0]}</span> : null}
                        {pair.redFlags[0] ? <span>{pair.redFlags[0]}</span> : null}
                      </div>
                    </td>
                    <td>
                      <button className="button-secondary" onClick={() => refreshDraft(pair)}>
                        {isBestForRole ? "Refresh role draft" : "Generate draft"}
                      </button>
                    </td>
                  </tr>
                );
              })}
            </tbody>
          </table>
        </div>
      </section>

      <section className="card tinted-card">
        <div className="section-head">
          <div>
            <p className="eyebrow">Auto-generated resume drafts</p>
            <h3>One revised draft per role using the best matching resume</h3>
            <p>These drafts stay in the app unless you copy the text or download a Word version.</p>
          </div>
        </div>
        {draftMessage ? (
          <p className={draftState === "error" ? "error-text" : "success-text"}>{draftMessage}</p>
        ) : null}
        {draftState === "loading" && !Object.keys(draftsByPairId).length ? <p>Generating revised drafts...</p> : null}
        <div className="stack">
          {bestPairs.map((pair) => {
            const draft = draftsByPairId[pair.pairId];
            return (
              <div key={pair.pairId} className="card inner-card">
                <div className="section-head">
                  <div>
                    <p className="eyebrow">{pair.company}</p>
                    <h4>{pair.roleName}</h4>
                    <p>
                      Built from <strong>{pair.resumeName}</strong> with a {pair.recommendation.toLowerCase()} recommendation.
                    </p>
                  </div>
                  {draft?.content ? (
                    <div className="action-row">
                      <button className="button-secondary" onClick={() => navigator.clipboard.writeText(draft.content)}>
                        Copy text
                      </button>
                      <button className="button" onClick={() => downloadDocx(draft)}>
                        Download .docx
                      </button>
                    </div>
                  ) : null}
                </div>
                {draft?.error ? <p className="error-text">{draft.error}</p> : null}
                {draft?.content ? (
                  <textarea className="draft-output" value={draft.content} readOnly rows="16" />
                ) : (
                  <p>{draftState === "loading" ? "Generating draft..." : "No draft available yet."}</p>
                )}
              </div>
            );
          })}
        </div>
      </section>
    </div>
  );
}

function getBestPairsForRun(run) {
  if (!run?.results?.roles?.length || !run?.results?.pairs?.length) return [];

  return run.results.roles
    .map((role) =>
      run.results.pairs.find((pair) => pair.roleId === role.id && pair.resumeId === role.bestResumeId)
    )
    .filter(Boolean);
}

function labelForMode(mode) {
  if (mode === "ats") return "ATS only";
  if (mode === "hr") return "HR fit only";
  return "Comprehensive";
}

function slugify(value) {
  return String(value || "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}
